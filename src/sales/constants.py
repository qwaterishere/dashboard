"""Константы домена sales.

Маппинг категорий iiko на три юнита дашборда: k (кухня), b (бар), w (вино).

⚠️ ВРЕМЕННОЕ РЕШЕНИЕ — ТРЕБУЕТ ПЕРЕРАБОТКИ (только для пилотного ресторана).
Список ниже захардкожен под справочник конкретной кассы и НЕ должен
попасть в тиражируемый Docker-образ. Целевое решение по стандарту
внедрения (docs/iiko-setup-standard.md): в iiko ресторана настраивается
дерево номенклатуры с корневыми группами Кухня/Бар/Вино, код читает
DishGroup.TopParent, и этот модуль удаляется целиком.
Условия удаления: пилот перестроил дерево -> выгрузка перешла
на TopParent -> resolve_cat() сведён к чтению родительской группы.

Пока модуль жив: новые категории по умолчанию падают в кухню,
проверяйте список при появлении новых.
"""

CAT_KITCHEN = 'k'
CAT_BAR = 'b'
CAT_WINE = 'w'

WINE_CATEGORIES = {
    'Вино белое', 'Вино красное', 'Вино розовое',
    'Игристые вина', 'Бокал вина', 'Вермут',
}

BAR_CATEGORIES = {
    'Бар', ',fh',  # ',fh' — «бар» в английской раскладке, живёт в справочнике iiko
    'Лимонады', 'Коктейли алк.', 'Коктейли безалк/молоч/смузи',
    'Пиво/К пиву', 'вода', 'Холодные напитки', 'Чай', 'Кофе', 'Айсти',
    'Виски', 'Водка', 'Джин/Ром', 'Коньяк/Ликеры', 'Текила',
    'Добавки бар',
}


def resolve_cat(dish_category: str) -> str:
    """Категория iiko -> юнит дашборда. Неизвестное -> кухня."""
    if dish_category in WINE_CATEGORIES:
        return CAT_WINE
    if dish_category in BAR_CATEGORIES:
        return CAT_BAR
    return CAT_KITCHEN
