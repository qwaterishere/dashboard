"""Контракт v2 страницы «Склад» — backend-todo №13.

Снапшот-модель: страница читает ежедневные слепки остатков из БД
(конец закрытого дня), а не живой iiko. ?date выбирает день слепка,
дефолт — последний. Производные — зона фронтенда: цена = value / qty,
доля склада = value / Σ totals.
"""

import datetime as dt
from typing import Literal

from pydantic import Field

from .base import StrictModel


class StoreValue(StrictModel):
    """Стоимость остатков одного юнита складов."""

    key: Literal['k', 'b', 'w'] = Field(
        description="Юнит склада: k кухня, b бар, w вино. "
        "Источник — маппинг физических складов при онбординге (по id); "
        "стандарт требует склады зеркально продажам (паспорт §9). "
        "В деньгах со своим юнитом продаж не сходится: сырьё одного "
        "блюда может хранится на разных складах"
    )
    value: float = Field(
        description="Стоимость ПОЛОЖИТЕЛЬНЫХ остатков; минусовые строки "
        "сюда не входят (см. negativeStock). Округлено до целой "
        "единицы валюты (агрегатные деньги)"
    )


class StockPosition(StrictModel):
    """Позиция слепка дня — как есть в учёте, ВКЛЮЧАЯ отрицательные."""

    productId: str = Field(
        description="UUID продукта в iiko — стабильная идентичность позиции: "
        "имя может меняться, id — никогда. Связь позиции между слепками "
        "разных дней — только по нему"
    )
    name: str = Field(
        description="Продукт — имя на момент синка. При переименовании "
        "в iiko новые слепки получают новое имя, старые хранят старое "
        "(история как была); идентичность — productId"
    )
    category: str = Field(
        description="Категория: ближайшая папка номенклатуры iiko "
        "(тоже слепок на момент синка)"
    )
    store: Literal['k', 'b', 'w'] = Field(description="Юнит склада")
    qty: float = Field(
        description="Остаток; дробное у весовых (4.5 кг); может быть "
        "ОТРИЦАТЕЛЬНЫМ — грязный складской учёт (см. negativeStock). "
        "Округлено до 3 знаков (точность весовых)"
    )
    unit: str = Field(description="Единица измерения: кг / л / шт / уп / …")
    value: float = Field(
        description="Стоимость остатка; цена за единицу = value / qty (фронт). "
        "Округлено до 2 знаков — чтобы производная цена за единицу "
        "не теряла точность"
    )


class NegativeStock(StrictModel):
    """Готовое предупреждение о минусовых остатках.

    Правило «минус не входит в totals» применяет бэкенд. Полный список
    минусовых строк — фильтр positions по qty < 0 (они там есть).
    Ноль везде — честный ноль: учёт чистый.
    """

    count: int = Field(description="Позиций с отрицательным остатком")
    valueAbs: float = Field(
        description="Суммарная «дыра» по модулю (сколько списано сверх "
        "оприходованного в деньгах). Округлено до 2 знаков"
    )


class DynamicsPoint(StrictModel):
    """Одна точка ГРАФИКА «стоимость склада во времени» (блок dynamics).

    Зачем: остатки — замороженные деньги, ценность в ТРЕНДЕ, не в разовом
    числе. Линия вверх = закупаем быстрее, чем расходуем (деньги мертвеют
    на полках, риск порчи — виден ДО инвентаризации); ступенька вверх =
    крупная закупка (можно открыть слепок того дня). Разрез по юнитам
    в каждой точке — чтобы фронт рисовал и общую линию (сумма byStore),
    и линии по складам из одних данных. Гранулярность неделя/месяц —
    прореживание точек на фронте, не отдельные данные.
    """

    date: dt.date = Field(description="День слепка")
    byStore: list[StoreValue] = Field(
        description="Всегда все три юнита k/b/w (нулевые включены); "
        "total фронт суммирует сам"
    )


class StockDataBounds(StrictModel):
    """Контракт day-picker'а: какие дни можно выбрать.

    earliest/latest задают границы календаря, availableDates — точный
    набор пикабельных дней внутри них. Day-picker гасит всё, чего нет
    в availableDates, поэтому запрос ?date всегда попадает в имеющийся
    слепок (404 при корректном пикере не случается).

    Ретеншн виден прямо в availableDates: свежая история — все дни,
    старше скользящего года — только воскресенья; плюс возможны дыры
    (заведение не работало, синк не прошёл).
    """

    earliest: dt.date | None = Field(
        description="Самый ранний слепок (глубина бэкфила) = min(availableDates); "
        "null — слепков нет. Для нижней границы календаря"
    )
    latest: dt.date | None = Field(
        description="Самый свежий слепок (последний закрытый день) = "
        "max(availableDates); null — слепков нет. Дефолт asOf и верхняя "
        "граница календаря"
    )
    availableDates: list[dt.date] = Field(
        description="Точный список дней, за которые слепок есть — "
        "day-picker гасит даты вне списка. Кодирует и ретеншн "
        "(воскресенья глубже года), и дыры (нерабочие дни, сбои синка)"
    )


class Warehouse(StrictModel):
    asOf: dt.date = Field(
        description="День показываемого слепка (конец этого закрытого дня); "
        "выбирается ?date, дефолт — latest. ?date обязан быть из "
        "dataBounds.availableDates, иначе 404"
    )
    dataBounds: StockDataBounds = Field(
        description="Контракт day-picker'а: границы календаря + точный "
        "список пикабельных дней (availableDates)"
    )
    totals: list[StoreValue] = Field(
        description="Всегда три элемента k/b/w (нулевые включены); "
        "только положительные остатки"
    )
    positions: list[StockPosition] = Field(
        description="ПОЛНЫЙ состав слепка, включая минусовые строки "
        "(~300–700 позиций), по убыванию value; сортировка/поиск — фронт. "
        "ВНИМАНИЕ: сумма positions ≠ totals — totals считает только "
        "положительные строки"
    )
    negativeStock: NegativeStock
    dynamics: list[DynamicsPoint] = Field(
        description="ДАННЫЕ ГРАФИКА динамики стоимости склада — "
        "единственный источник линии (снапшот-блоки её не кормят). "
        "Точки по дням за ?from/?to (дефолт — 30 дней до asOf); "
        "режим недели/месяца — прореживание на фронте"
    )
